def MatrixBacktrack_Local(v, w, PAM250, indel):
    S = {}
    backtrack = {}
    S[(0,0)] = 0
    for i in range(1,len(v)+1):
        S[(i,0)] = 0
        backtrack[i,0] = "source"
    for j in range(1,len(w)+1):
        S[(0,j)] = 0
        backtrack[0,j] = "source"
    for i in range(1,len(v)+1):
        for j in range(1,len(w)+1):
            t = PAM250[v[i-1]][w[j-1]]
            S[(i,j)] = max(0, S[(i-1,j)]-indel, S[(i,j-1)]-indel, S[(i-1,j-1)]+t)
            if S[(i,j)] == 0:
                backtrack[i,j] = "source"
            elif S[(i,j)] == S[(i-1,j)]-indel:
                backtrack[i,j] = "↓"
            elif S[(i,j)] == S[(i,j-1)]-indel:
                backtrack[i,j] = "→"
            elif S[(i,j)] == S[(i-1,j-1)]+t:
                backtrack[i,j] = "↘"
    r = -math.inf
    for node,score in S.items():
        if score > r:
            r = score
            endnode = node
    print(r)                                                        # the highest score
    S[endnode] = r
    backtrack[(len(v),len(w))] = endnode
    return backtrack, S

def LocalAlignment(backtrack, v, w, i, j):
    str_v = []
    str_w = []
    while backtrack[(i,j)] != 'source':
        if backtrack[(i,j)] == "↓":
            str_v.append(v[i-1])
            str_w.append("-")
            i -= 1
        elif backtrack[(i,j)] == "→":
            str_v.append("-")
            str_w.append(w[j-1])
            j -= 1
        else:
            str_v.append(v[i-1])
            str_w.append(w[j-1])
            i -= 1
            j -= 1
    return str_v, str_w


